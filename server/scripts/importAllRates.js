async function importRates() {
    console.log('Starting comprehensive rate import...');
    
    // Clear existing rates
    await db.delete(rateTables);
    
    // Parse and import material rates
    const materialRates = parseTMData(tmRatesData, 'material', 'General Materials');
    await insertRateTable('FieldFlo General Materials - Complete', 'material', materialRates);
    
    // Parse and import equipment rates
    const equipmentRates = parseTMData(equipmentRatesData, 'equipment', 'Equipment');
    await insertRateTable('FieldFlo Equipment - Complete', 'equipment', equipmentRates);
    
    // Parse and import chemical rates
    const chemicalRates = parseTMData(chemicalRatesData, 'material', 'Chemicals');
    await insertRateTable('FieldFlo Chemicals - Complete', 'material', chemicalRates);
    
    // Parse and import machinery rates
    const machineryRates = parseTMData(machineryRatesData, 'material', 'Machinery');
    await insertRateTable('FieldFlo Machinery Materials - Complete', 'material', machineryRates);
    
    console.log('Rate import completed successfully!');
}

function parseTMData(data, type, category) {
    const lines = data.trim().split('\n');
    const rates = [];
    
    for (const line of lines) {
        if (line.startsWith('#') || line.trim() === '') continue;
        
        const parts = line.split(',');
        if (parts.length >= 4) {
            const [id, description, unit, rate] = parts;
            rates.push({
                code: `TM-${id.trim()}`,
                description: description.trim().replace(/;/g, ','),
                rate: parseFloat(rate.trim()),
                unit: unit.trim().toLowerCase(),
                category: category
            });
        }
    }
    
    return rates;
}

async function insertRateTable(name, type, ratesData) {
    await db.insert(rateTables).values({
        name: name,
        type: type,
        data: ratesData,
        region: 'Southern CA',
        effectiveDate: new Date('2024-07-01'),
        isApproved: true,
        reviewedBy: 1,
        reviewedAt: new Date()
    });
    
    console.log(`Inserted ${name} with ${ratesData.length} rates`);
}

